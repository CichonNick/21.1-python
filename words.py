


# this will print each word uppercase

words = ["every" , "people" , "effectively" , "efficent"]
def print_upper_words(words):
    for word in words:
        print(word.upper())

# This funtion will print only the words that start with the letter "e" or "E"

def print_upper_words(words):
    for word in words:
        if word.startswith("e") or word.startswith("E"):
            print(word.upper())

